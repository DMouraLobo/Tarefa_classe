package package1;

public class Carro {

	//Declaração das variaveis da classe
	private int numDePortas;
	private String marca;
	private String modelo;
	private String cor;
	private int ano;
	
	//Inicio métodos getters and setters
	public int getNumDePortas() {
		return numDePortas;
	}
	
	public void setNumDePortas(int numDePortas) {
		this.numDePortas = numDePortas;
	}
	
	public String getMarca() {
		return marca;
	}
	
	public void setMarca(String marca) {
		this.marca = marca;
	}
	
	public String getModelo() {
		return modelo;
	}
	
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	
	public String getCor() {
		return cor;
	}
	
	public void setCor(String cor) {
		this.cor = cor;
	}
	
	public int getAno() {
		return ano;
	}
	
	public void setAno(int ano) {
		this.ano = ano;
	}
	//Fim dos métodos getters and setters 
	
}
